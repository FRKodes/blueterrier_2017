				<p>Para tener una idea de la magnitud y el poder que ha alcanzado esta plataforma, en algunas estadísticas que se llevaron a cabo en marzo de este año (2020), Tik Tok se encuentra disponible en más de 141 países, con nada más y nada menos que un promedio de 800 millones de usuarios activos al mes, según el datareportal2020. </p>
				<p>Vale la pena reconocer la diferencia entre un influencer y un microinfluencer, aquí despejamos dudas:</p>
				
				<h2 class="block-title blue_1">Influencer</h2>
				
				<ul>
					<li>Tiene más de 100 mil seguidores en sus plataformas.</li>
					<li>No tiene tanta interacción en publicaciones con sus seguidores ya que por la cantidad de comentarios que puede recibir, no se le brinda respuesta a todos.</li>
					<li>La cantidad de personas que comparten sus contenidos es notoria y significativa.</li>
					<li>Suelen ser personalidades con look de inalcanzables, aumentando la popularidad entre sus seguidores.</li>
				</ul>
				
				<h2 class="block-title blue_1">Microinfluencers</h2>

				<ul>
					<li>Tienen entre mil y 10 mil seguidores en sus plataformas.</li>
					<li>Su contenido es inspiracional y siempre dirigido a la interacción cercana con sus seguidores.</li>
					<li>Al no tener tanta audiencia como un influencer, la interacción por parte de sus seguidores en sus redes sociales es menor, volviéndose más fácil de responder comentarios y hacerlo sentir como ‘’una persona normal’’.</li>
					<li>Aunque su contenido se comparta en menor cantidad, sigue siendo significativo.</li>
				</ul>

